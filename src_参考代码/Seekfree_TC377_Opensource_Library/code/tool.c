// 函数简介     限制范围
// 参数说明     value
// 参数说明     min
// 参数说明     max
// 返回类型     int
// 使用示例     my_limit(min, max);
// 备注信息
//-------------------------------------------------------------------------------------------------------------------
int my_limit(int value, int min, int max)
{
    if (min < max)
    {
        if (value <= min)
        {
            value = min;
        }
        if (value >= max)
        {
            value = max;
        }
    }

    return value;
}

