void circle_point(uint16 y, uint16 x)
{
    for (uint16 i = y - 5; i <= y + 5; i++)
    {
        ips200_draw_point(x - 5, i, RGB565_PURPLE);
        ips200_draw_point(x + 5, i, RGB565_PURPLE);
    }

    for (uint16 j = x - 5; j <= x + 5; j++)
    {
        ips200_draw_point(j, y - 5, RGB565_PURPLE);
        ips200_draw_point(j, y + 5, RGB565_PURPLE);
    }
}
